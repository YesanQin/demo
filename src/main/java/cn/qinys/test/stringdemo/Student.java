package cn.qinys.test.stringdemo;

public class Student {
	private String name;
	
	private int age;
	
	private boolean gender;
	
	private String farvity;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public boolean isGender() {
		return gender;
	}

	public void setGender(boolean gender) {
		this.gender = gender;
	}

	public String getFarvity() {
		return farvity;
	}

	public void setFarvity(String farvity) {
		this.farvity = farvity;
	}
}
